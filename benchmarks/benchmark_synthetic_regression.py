"""Synthetic regression benchmark.

Tracks RepLeafGBM's leaf models, encoders (fixed + learned), and robust
objectives across development on a small synthetic signal. Writes a dated
markdown report next to the heavier suites.

Run from the repository root:
    python3 benchmarks/benchmark_synthetic_regression.py [--quick]
    python3 benchmarks/benchmark_synthetic_regression.py --contamination 0.08
"""

from __future__ import annotations

import numpy as np
from common import (
    apply_quick,
    contaminate,
    external_gbm_models,
    make_parser,
    print_table,
    synthetic_tabular,
    time_model,
    write_markdown_report,
)
from sklearn.ensemble import (
    GradientBoostingRegressor,
    HistGradientBoostingRegressor,
    RandomForestRegressor,
)

from repleafgbm import RepLeafRegressor


def rmse(y, p):
    return float(np.sqrt(np.mean((y - p) ** 2)))


def repleaf(leaf_model: str, encoder: str, n_estimators: int, seed: int,
            objective: str | None = None) -> RepLeafRegressor:
    # Encoder settings (n_bins, max_leaf_emb_dim) are library defaults on
    # purpose: the benchmark tracks what users get out of the box.
    return RepLeafRegressor(
        n_estimators=n_estimators,
        learning_rate=0.1,
        num_leaves=31,
        min_samples_leaf=20,
        leaf_model=leaf_model,
        encoder=encoder,
        objective=objective,
        random_state=seed,
    )


def main() -> None:
    args = apply_quick(make_parser(__doc__).parse_args())
    rng = np.random.default_rng(args.seed)

    X, signal = synthetic_tabular(args.n_train + args.n_test, args.n_features, rng)
    y_clean = signal + rng.normal(0.0, 0.3, len(signal))
    Xtr, Xte = X[: args.n_train], X[args.n_train :]
    ytr, yte = y_clean[: args.n_train], y_clean[args.n_train :]
    # Contaminate only the TRAIN targets; the clean test target measures how well
    # the robust objectives shrug off training outliers.
    if args.contamination > 0.0:
        ytr = contaminate(ytr, args.contamination, 8.0, rng)

    n, seed = args.n_estimators, args.seed
    models: list[tuple[str, object]] = [
        ("sklearn GradientBoosting", GradientBoostingRegressor(
            n_estimators=n, random_state=seed)),
        ("sklearn HistGradientBoosting", HistGradientBoostingRegressor(
            max_iter=n, random_state=seed)),
        ("sklearn RandomForest", RandomForestRegressor(
            n_estimators=n, random_state=seed, n_jobs=-1)),
        ("RepLeaf constant", repleaf("constant", "identity", n, seed)),
        ("RepLeaf embedded_linear identity",
         repleaf("embedded_linear", "identity", n, seed)),
        ("RepLeaf embedded_linear plr", repleaf("embedded_linear", "plr", n, seed)),
        ("RepLeaf embedded_linear periodic",
         repleaf("embedded_linear", "periodic", n, seed)),
        ("RepLeaf embedded_linear cross", repleaf("embedded_linear", "cross", n, seed)),
        ("RepLeaf constant huber", repleaf("constant", "identity", n, seed, "huber")),
        ("RepLeaf constant quantile(0.5)",
         repleaf("constant", "identity", n, seed, "quantile")),
    ]
    try:  # learned encoders when the [torch] extra is present
        import torch  # noqa: F401

        models += [
            ("RepLeaf embedded torch_periodic_plr",
             repleaf("embedded_linear", "torch_periodic_plr", n, seed)),
            ("RepLeaf embedded torch_mlp",
             repleaf("embedded_linear", "torch_mlp", n, seed)),
        ]
    except ImportError:
        pass
    models += external_gbm_models("regression", n, seed)

    results = [
        time_model(name, m, Xtr, ytr, Xte, yte, {"rmse": rmse}, lambda m, X: m.predict(X))
        for name, m in models
    ]
    header = (
        f"synthetic regression: n_train={args.n_train} n_test={args.n_test} "
        f"n_features={args.n_features} n_estimators={n} "
        f"contamination={args.contamination:.0%}"
    )
    print("\n" + header + "\n")
    print_table(results)
    preamble = [
        "Auto-generated by `benchmarks/benchmark_synthetic_regression.py` "
        "(indicative synthetic numbers, not a leaderboard). " + header + ". "
        "Robust objectives (huber/quantile) shine when `--contamination` injects "
        "training outliers; the test target stays clean."
    ]
    out = write_markdown_report("synthetic-regression",
                                "Synthetic regression benchmark", preamble, results)
    print(f"\nreport written to {out}")


if __name__ == "__main__":
    main()
